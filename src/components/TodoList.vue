<template>
  <div>
    <input
      type="text"
      class="todo-input"
      placeholder="What needs to be done"
      v-model="newTodo"
      @keyup.enter="addTodo"
    />
    <div v-for="(todo, index) in todos" :key="todo.id" class="todo-item">
      <div>{{ todo.title }}</div>
      <div>
        {{ todo.requiredTime }}
      </div>
      <div class="remove-item" @click="removeTodo(index)">
        &times;
      </div>
    </div>
  </div>
</template>

<script>
import { uuid } from "vue-uuid";

export default {
  name: "TodoList",
  data() {
    return {
      newTodo: "",
      newRequiredTime: 0,
      todos: [
        {
          id: 1,
          title: "Finish test",
          isComplete: false,
          requiredTime: 60,
        },
        {
          id: 2,
          title: "have lunch",
          isComplete: false,
          requiredTime: 15,
        },
      ],
    };
  },

  methods: {
    addTodo() {
      // prevent addition on empty input
      if (this.newTodo.trim().length === 0) {
        return;
      }

      this.todos.push({
        id: uuid.v4(),
        title: this.newTodo,
        requiredTime: this.newRequiredTime,
        isComplete: false,
      });
      this.newTodo = "";
      this.newRequiredTime = 0;
    },
    removeTodo(index) {
      this.todos.splice(index, 1);
    },
  },
};
</script>

